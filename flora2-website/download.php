<h1>
Download FLORA-2
</h1>
<pre>

</pre>
<p>
  FLORA-2 relies on the XSB inference engine to run.
  One can install either the latest released version of FLORA-2,
  Version 0.95 (Androcymbium), or the current development version from CVS.
</p>
<p>
   The FLORA-2 Visualizer and Editor has its own <a href="visualizer.html">page
   with download instructions</a>.
</p>


<h3>Downloading the latest release of FLORA-2:</h3>
<p>
Please follow these steps:
<ul>
  <li>
  Download and install <a
  href="http://sourceforge.net/project/showfiles.php?group_id=1176"
  target="new_window">
  Release 3.1 of XSB or later</a>.
  </li>
  <li>
  Download the <a
  href="http://sourceforge.net/project/showfiles.php?group_id=50604"
  target="new_window">latest release</a> of FLORA-2 into a separate directory
  and follow the <a href="installation.html" target="new_window">installation
  instructions</a>.
  </li>
  <li>
  Take a look at the <a href="release_notes.html#release"
  target="new_window">release notes</a> for the list of changes, bug fixes, and
  incompatibilities with previous versions.
  </li>
</ul>
</p>


<h3>Downloading the current development version from CVS:</h3>
<p>
  This version contains the latest features, but might not always be
  stable.  However,
  this allows you to track the development of FLORA-2 and contribute!
  You can install the released version and
  the CVS version on the same system and switch between them at will
  (but not within the same session of XSB).
  The development version of FLORA-2 often requires that you use the
  latest development version of XSB directly from the <a
  href="http://sourceforge.net/cvs/?group_id=1176" target="new_window"> XSB CVS
  repository</a>. (Under Windows, configuring the CVS version of XSB requires
  Microsoft's Visual Studio.)
</p>
<p>
  Check out the latest version of the <tt>flora2</tt> module from the <a
  href="http://sourceforge.net/cvs/?group_id=50604" target="new_window">
    CVS repository</a> into a separate directory and follow the <a
  href="installation.html" target="new_window">installation instructions</a>.
  Note: the development version downloaded from CVS is not regularly 
  tested under Windows. Please report to us any problems (and fixes!).
</p>


