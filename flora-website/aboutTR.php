<h1>
Transaction Logic
</h1>
<pre>

</pre>
<p>
  Transaction logic provides a logical foundation for state changes and side
  effects in a logic programming language. A significant portion of this
  theory is implemented in FLORA-2.
  Applications of Transaction Logic include modeling and reasoning about
  workflows, planning, robotics, view maintenance in databases, and
  more. Some works describing these applications can be found <a
  href="relatedWorks.php">here</a>.
</p>

<h3>Some Papers on Transaction Logic</h3>
<ul>
  <li>
    <a
    href="http://www.cs.sunysb.edu/~kifer/TechReports/transaction-logic.pdf">Transaction
    Logic</a>. This is the most comprehensive report that describes this logic.
  </li>
  <li>
    <a
    href="http://www.cs.sunysb.edu/~kifer/TechReports/tr-chomicki.pdf">Logic
    Programming for Database Transactions</a> 
    in Logics for Databases and Information Systems, J. Chomicki and G. Saake
    (eds.), Kluwer Academic Publ., 1998.
    <br>
    This is an introduction to Transaction Logic, much shorter than the above
    report with a focus on database applications.
  </li>
  <li>
    <a
    href="http://citeseer.ist.psu.edu/bonner98results.html">Results
    on Reasoning about Updates in Transaction Logic</a> 
    in Transactions and Change in
          Logic Databases, Lecture Notes in Computer Science, vol. 1472,
          Springer Verlag, 1998.
  </li>
  <li>
    <a href="http://www.cs.sunysb.edu/~kifer/TechReports/concurrent-trans-logic.pdf">
      Concurrent Transaction Logic
    </a>. Joint Intl. Conference and Symposium on
            Logic Programming, Bonn, Germany, September 1996.
    <br>
    This is an extension of Transaction Logic, which allows parallelism in the
    execution of complex actions. This extension is not implemented in
    FLORA-2, but an independent implementation can be found 
    <a href="http://www.cs.toronto.edu/~bonner/ctr/index.html">here</a>.
    One important use of Concurrent Transaction Logic is in 
    <a href="http://citeseer.ist.psu.edu/davulcu98logic.html">
      modeling and verification of workflows</a> (Intl. ACM Conference on
      Principles of Database Systems, 1998).
  </li>
<li>  
   <a href="http://www.www2004.org/proceedings/docs/2p144.pdf">
     CTR-S: A Logic for Specifying Contracts in Semantic Web Services
   </a>
   Proceddings of the 13-th World Wide Web Conference (WWW2004), May 2004.
</li>
<li>
   Older papers:
</li>
<ul>
  <li>
     <a href="http://citeseer.ist.psu.edu/bonner93transaction.html">
       Transaction Logic Programming,
     </a>
     International Conference on Logic Programming, 1993.
  </li>
<li>  
   <a href="http://citeseer.ist.psu.edu/bonner94overview.html">
     An Overview of Transaction Logic,
     </a>
   Theoretical Computer Science, 133:2, 1994.
</li>
<li>
   <a
   href="http://www.cs.sunysb.edu/~kifer/TechReports/tr-db-programming.pdf">
     Database Programming in Transaction Logic</a>.  Proceedings of the Fourth
     International Workshop on Database Programming Languages - Object Models
     and Languages, 1993.
</li>
</ul>
</ul>

